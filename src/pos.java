    private void sales() {
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd");
        LocalDateTime now = LocalDateTime.now();
        String date = dtf.format(now);
        
        String subtot = txtsub.getText();
        String cashier = username.getText();
        String pay = txtpay.getText();
        String bal = txtbal.getText();

        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/inventory", "root", "password");
            
            String query = "insert into sales(date, cashier, subtotal, pay, balance)values(?,?,?,?,?)";
            pst = con.prepareStatement(query);

            pst.setString(1, date);
            pst.setString(2, cashier);
            pst.setString(3, subtot);
            pst.setString(4, pay);
            pst.setString(5, bal);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Record Saved");
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    private void tableUpdate() {
        //update the product table
        model = (DefaultTableModel) recordTable.getModel();
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con =  DriverManager.getConnection("jdbc:mysql://localhost/inventory", "root", "password");
            //in below line qty = qty - recordTable.getValueAt(i, 3); so qty updated
            pst = con.prepareStatement("update product set qty = qty - ? where barcode = ?");
            
            for(int i=0; i<recordTable.getRowCount(); i++) {
                String productid = (String) recordTable.getValueAt(i, 0);
                String qty =  (String) recordTable.getValueAt(i, 3);
                pst.setString(1, qty);
                pst.setString(2, productid);
                pst.executeUpdate();
            }
            pst.addBatch();
            JOptionPane.showMessageDialog(null, "Product set Updated");
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(pos.class.getName()).log(Level.SEVERE, null, ex);
        }
                
        
    }
    
