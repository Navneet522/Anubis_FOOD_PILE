    private void btProDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btProDetailActionPerformed
        // TODO add your handling code here:
        //product detail table
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/inventory", "root", "password");
            pst = con.prepareStatement("SELECT * from  product WHERE price >= 0 ORDER BY price");
            ResultSet rs = pst.executeQuery();
            java.sql.ResultSetMetaData rsd = rs.getMetaData();
            int c = rsd.getColumnCount();
            
            DefaultTableModel d = (DefaultTableModel) infotable.getModel();
           
            for (int i = infotable.getRowCount()-1; i >=0; i--){
                d.removeRow(i);
            }

            while(rs.next()) {
                Vector v = new Vector();
                
                for(int i=0; i<c; i++) {
                    v.add(rs.getString("barcode"));
                    v.add(rs.getString("product"));
                    v.add(rs.getString("price"));
                    v.add(rs.getString("qty"));
                }
                d.addRow(v);
            }
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(sorting.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btProDetailActionPerformed

